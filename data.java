package datatypes1;

public class data {
    public static void main(String[] args) {
        int a=20;
        int c= 40;

        System.out.println(a+c);//60
        System.out.println(a-c);//-20
        System.out.println(a*c);//800
        System.out.println(a/c);//0
        System.out.println(a++);//20
        System.out.println(a--);//21
    }
}
